
public class Product {
	private String  sName;
	private  String sId;
	
	public Product(String sName, String sId) {
		super();
		this.sName = sName;
		this.sId = sId;
	}
	
	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public String getsId() {
		return sId;
	}
		public void setsId(String sId) {
		this.sId = sId;
	}
	
	
	
	

}
